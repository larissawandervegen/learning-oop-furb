    private void jbCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCalcularActionPerformed
        // TODO add your handling code here:
        
        String nome = tfNome.getText();
        double salario = Double.parseDouble(tfSalario.getText());
        int indice = Integer.parseInt(lbIndice.getText());
        
        if (indice <= 4){
           Funcionario func1 = new Funcionario();
           func1.setNome(nome);
           func1.setSalario(salario);
        
            vetFuncionarios[indice] = func1;
            tfNome.setText("");
            tfSalario.setText("");
            
            if (indice == 4){
                 double valorImposto = func1.calcularIrpf();
                DecimalFormat df = new DecimalFormat("0.00");
                String mensagem = "";
                
                for (int i = 0; i < vetFuncionarios.length; i++){
                  mensagem += "Nome: "+ vetFuncionarios[i].getNome()+
                        " Salario: "+df.format(vetFuncionarios[i].getSalario())+
                        " Imposto: "+df.format(vetFuncionarios[i].calcularIrpf())+"\n";
                }
                
                JOptionPane.showMessageDialog(rootPane, mensagem);
            }
            indice++;
            lbIndice.setText(String.valueOf(indice));  
            
           
        }
        
        
       
        
        
    }//GEN-LAST:event_jbCalcularActionPerformed

